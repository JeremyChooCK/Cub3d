#include "libft.h"

void	ft_lstadd_back(t_map **lst, t_map *new)
{
	t_map	*aux;

	if (!new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	aux = ft_lstlast(*lst);
	aux->next = new;
}
