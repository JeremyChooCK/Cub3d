#include "libft.h"

t_map	*ft_lstmap(t_map *lst, void *(*f)(void *), void (*del)(void *))
{
	t_map	*new_lst;
	t_map	*elem;

	if (!lst)
		return (0);
	new_lst = 0;
	while (lst)
	{
		elem = ft_lstnew(f(lst->content));
		if (!elem)
		{
			ft_lstclear(&new_lst, del);
			return (0);
		}
		ft_lstadd_back(&new_lst, elem);
		lst = lst->next;
	}
	return (new_lst);
}
