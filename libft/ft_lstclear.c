#include "libft.h"

void	ft_lstclear(t_map **lst, void (*del)(void*))
{
	t_map	*aux;

	if (!*lst)
		return ;
	while (*lst)
	{
		aux = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = aux;
	}
	*lst = 0;
}
